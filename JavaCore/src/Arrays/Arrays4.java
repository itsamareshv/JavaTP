package Arrays;

public class Arrays4 {
	public static void main(String[] args) {
		int a[] = new int[10];
		String b[] = new String[10];

		a[0] = 10;
		b[0] = "String";
		System.out.println(a[0]);
		System.out.println(b[0]);
		System.out.println(a);
		System.out.println(b);
		
		
	}
}
